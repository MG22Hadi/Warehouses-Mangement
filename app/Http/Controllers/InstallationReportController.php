            // if (!$user->hasRole('manager')) { ... }

            DB::transaction(function () use ($report, $user) {
                // 3. ุฎุตู ุงููููุงุช ูุชุณุฌูู ุงูุญุฑูุงุช ููุท ุฅุฐุง ูุงู ููุน ุงูุชูุฑูุฑ 'stock_usage'
