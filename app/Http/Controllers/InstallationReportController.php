                        $location = $productLocation->location;

                        // ุฎุตู ุงููููุฉ
                        $productLocation->decrement('quantity', $quantity);
                        $location->decrement('used_capacity_units', $quantity);

                        // ุฑุณุงูุฉ ุชูุถุญ ูู ุฃู ูููุน ุชู ุงูุฎุตู
