                            ->first();

                        if (!$productLocation || $productLocation->quantity < $material->quantity) {
                            $availableQuantity = $productLocation ? $productLocation->quantity : 0;
                            throw new \Exception("ุงููููุฉ ุบูุฑ ูุชููุฑุฉ ูู ุงููููุน ุงููุญุฏุฏ ููููุชุฌ: {$material->product->name} (ุงููุชุงุญ: {$availableQuantity})");
